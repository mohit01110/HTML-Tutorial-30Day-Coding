for (let i = 1; i <= 10; i++) {
  console.log(`2 x ${i} = ${2 * i}`) 

}
//2. While Loop
let j = 1
while (j <= 10) {
  console.log(`3 x ${j} = ${3 * j}`) 
  j++
}

//3. Do While Loop
let k = 1
do {
  console.log(`4 x ${k} = ${4 * k}`) 
  k++
} while (k <= 10)   

